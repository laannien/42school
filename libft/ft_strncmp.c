#include "libft.h"

int		ft_strncmp(char const *s1, char const *s2, size_t n)
{
	unsigned char	*p1;
	unsigned char	*p2;
	size_t			i;

	p1 = (unsigned char *)s1;
	p2 = (unsigned char *)s2;
	i = 0;
	while (*p1 == *p2 && *p1 && *p2 && i < n)
	{
		p1++;
		p2++;
		i++;
	}
	return (*p1 - *p2);
}
