#include "libft.h"

int		ft_strncmp(char const *s1, char const *s2, size_t n)
{
	size_t i;

	i = 0;
	while (*s1 == *s2 && *s1 != '\0' && *s2 != '\0' && i < n)
	{
		s1++;
		s2++;
		i++;
	}
	return (*s1 - *s2);
}
