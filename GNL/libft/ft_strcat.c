#include "libft.h"

char	*ft_strcat(char *s1, char const *s2)
{
	char *arr;

	arr = s1;
	while (*arr)
	{
		arr++;
	}
	while (*s2)
	{
		*arr = *s2;
		arr++;
		s2++;
	}
	*arr = '\0';
	return (s1);
}
