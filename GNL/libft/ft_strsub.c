#include "libft.h"

char	*ft_strsub(char const *s, unsigned int start, size_t len)
{
	char	*result;
	size_t	i;

	if (!s)
	{
		return (NULL);
	}
	i = 0;
	result = (char*)malloc(sizeof(*s) * (len + 1));
	if (!result)
	{
		return (NULL);
	}
	while (i < len)
	{
		result[i] = s[start + i];
		i++;
	}
	result[i] = '\0';
	return (result);
}
